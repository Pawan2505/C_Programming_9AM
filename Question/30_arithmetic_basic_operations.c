#include <stdio.h>

int main() {

    int x = 10;
    int y = 5;

    printf("Sum: %d\n", x + y);       // 15
    printf("Sub: %d\n", x - y);       // 5
    printf("Mul: %d\n", x * y);       // 50
    printf("Div: %d\n", x / y);       // 2
    printf("Remainder: %d\n", x % y); // 0

    return 0;
}
